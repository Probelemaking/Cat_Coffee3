package Entities;

public class LocationDate {
    protected int year, mouth, day, hour, minute;

    LocationDate() {
    }

    public LocationDate(int year, int mouth, int day, int hour, int minute) {
        this.year = year;
        this.mouth = mouth;
        this.day = day;
        this.hour = hour;
        this.minute = minute;
    }

}

